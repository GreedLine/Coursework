$(document).ready(function() {

$(document).on('click', '.button-text', function() {
	$('.modal').css('display', 'block');
});




$(document).on('click', '.window-footer', function() {
	$('.modal').css('display', 'none');
});

$(document).on('click', '.window-head-right', function() {
	$('.modal').css('display', 'none');
});

});



